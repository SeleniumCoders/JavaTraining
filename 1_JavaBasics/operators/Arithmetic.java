package operators;

public class Arithmetic {

	public static void main(String[] args) {
			
		int a = 10;
		int b = 3;
		
		
		float res;
		
		res = a+b;
		System.out.println("a+b = " + res);
		
		res = a-b;
		System.out.println("a-b = " + res);
		
		res = a*b;
		System.out.println("a*b = " + res);
		
		
		res = a/b;
		System.out.println("a/b = " + res);
		
		
		res = a%b;
		System.out.println("a%b = " + res);

				
		
	}

}
