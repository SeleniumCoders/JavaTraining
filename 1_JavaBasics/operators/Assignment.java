package operators;

public class Assignment {

	public static void main(String[] args) {
		
		int a = 10;
		System.out.println("a = " + a);
		
		a += 5;   //a=a+5
		System.out.println("a += 5 = " + a);
		
		a -= 3;    //a=a-3
		System.out.println("a -= 3  = " + a);
		
	
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
